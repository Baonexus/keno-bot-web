import os
import threading
from flask import Flask
from bot import start_bot

app = Flask(__name__)

@app.route("/")
def home():
    return "Bot đang chạy OK!"

def run_bot():
    start_bot()

if __name__ == "__main__":
    threading.Thread(target=run_bot).start()
    port = int(os.environ.get("PORT", 10000))
    app.run(host="0.0.0.0", port=port)
